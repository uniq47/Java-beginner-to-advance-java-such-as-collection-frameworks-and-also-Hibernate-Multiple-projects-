package Session2;

public class conditionalstatement {
	
	public static void dif(int a, int b) 
	{
		
		int c = a-b;
		System.out.println(c);
	}
	
	public static void multi(int a , float b) {
		float c = a*b;
		System.out.println(c);
	}
	
	public static void div(float a, float b) {
		float c = a/b;
		System.out.println(c);
	}
	
	public static void main(String[] args)
	{
		
	dif(2,3);
	multi(2,3);
	div(2,3);
	
}
}
